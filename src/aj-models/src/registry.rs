    fn empty_overrides() -> OverridesFile {
        OverridesFile { overrides: vec![] }
    }

    #[test]
    fn registry_lookup_and_listing() {
        let cat = Catalog {
            // updated_at: 0 suppresses the staleness warning in tests.
            updated_at: 0,
            source: "test".into(),
            models: vec![
                sample_model("anthropic", "claude-x"),
                sample_model("anthropic", "claude-y"),
                sample_model("openai", "gpt-z"),
            ],
        };
        let reg = ModelRegistry::from_catalog_with_overrides(cat, empty_overrides(), "test");

        // Lookups
        assert!(reg.get("anthropic", "claude-x").is_some());
        assert!(reg.get("openai", "gpt-z").is_some());
        assert!(reg.get("openai", "gpt-missing").is_none());
        assert!(reg.get("nope", "x").is_none());

        // Stable ordering
        assert_eq!(reg.providers(), vec!["anthropic", "openai"]);
        let anthropic_ids: Vec<_> = reg
            .models("anthropic")
            .iter()
            .map(|m| m.id.as_str())
            .collect();
        assert_eq!(anthropic_ids, vec!["claude-x", "claude-y"]);
        assert!(reg.models("nope").is_empty());
    }

    #[test]
    fn overrides_shallow_merge() {
        let cat = Catalog {
            updated_at: 0,
            source: "test".into(),
            models: vec![sample_model("anthropic", "claude-opus")],
        };
        let overrides = OverridesFile {
            overrides: vec![OverrideEntry {
                target: OverrideTarget {
                    provider: "anthropic".into(),
                    id: "claude-opus".into(),
                },
                patch: OverridePatch {
                    supports_xhigh: Some(true),
                    supports_adaptive_thinking: Some(true),
                    cost: Some(ModelCost {
                        input: 5.0,
                        output: 25.0,
                        cache_read: 0.5,
                        cache_write: 6.25,
                    }),
                    ..Default::default()
                },
                reason: "test".into(),
            }],
        };
        let reg = ModelRegistry::from_catalog_with_overrides(cat, overrides, "test");
        let m = reg.get("anthropic", "claude-opus").unwrap();
        assert!(m.supports_xhigh);
        assert!(m.supports_adaptive_thinking);
        assert_eq!(m.cost.cache_read, 0.5);
        // Untouched fields remain.
        assert_eq!(m.context_window, 200_000);
        assert_eq!(m.input, vec![InputModality::Text]);
    }

    #[test]
    fn overrides_target_unknown_is_silent() {
        let cat = Catalog {
            updated_at: 0,
            source: "test".into(),
            models: vec![sample_model("anthropic", "real-model")],
        };
        let overrides = OverridesFile {
            overrides: vec![OverrideEntry {
                target: OverrideTarget {
                    provider: "anthropic".into(),
                    id: "ghost-model".into(),
                },
                patch: OverridePatch {
                    supports_xhigh: Some(true),
                    ..Default::default()
                },
                reason: "test".into(),
            }],
        };
        // Should not panic; the matching entry is preserved.
        let reg = ModelRegistry::from_catalog_with_overrides(cat, overrides, "test");
        assert!(reg.get("anthropic", "real-model").is_some());
        assert!(reg.get("anthropic", "ghost-model").is_none());
    }

    #[test]
    fn calculate_cost_basic() {
        let model = sample_model("anthropic", "claude-x");
        let mut usage = Usage {
            input: 1_000_000,
            output: 500_000,
            cache_read: 100_000,
            cache_write: 50_000,
            total_tokens: 0,
            cost: Default::default(),
        };
        calculate_cost(&model, &mut usage);
        assert!((usage.cost.input - 3.0).abs() < 1e-9);
        assert!((usage.cost.output - 7.5).abs() < 1e-9);
        assert!((usage.cost.cache_read - 0.03).abs() < 1e-9);
        assert!((usage.cost.cache_write - 0.1875).abs() < 1e-9);
        let expected_total = 3.0 + 7.5 + 0.03 + 0.1875;
        assert!((usage.cost.total - expected_total).abs() < 1e-9);
    }

    #[test]
    fn capability_probes() {
        let mut m = sample_model("anthropic", "x");
        assert!(!supports_xhigh(&m));
        assert!(!supports_adaptive_thinking(&m));
        m.supports_xhigh = true;
        m.supports_adaptive_thinking = true;
        assert!(supports_xhigh(&m));
        assert!(supports_adaptive_thinking(&m));
    }

    #[test]
    fn models_are_equal_identity_not_state() {
        let mut a = sample_model("anthropic", "claude-x");
        let mut b = sample_model("anthropic", "claude-x");
        // State diverges, identity doesn't.
        a.cost.input = 100.0;
        b.context_window = 1;
        assert!(models_are_equal(&a, &b));
        b.id = "claude-y".into();
        assert!(!models_are_equal(&a, &b));
    }

    /// The bundled seed and overrides are part of the binary; if either
    /// fails to parse the registry won't load. Regression-test that the
    /// committed JSON stays well-formed.
    #[test]
    fn bundled_seed_and_overrides_parse() {
        let seed: Catalog = serde_json::from_str(SEED_MODELS_JSON).expect("seed parses");
        let overrides: OverridesFile =
            serde_json::from_str(OVERRIDES_JSON).expect("overrides parse");
        // Seed must contain at least one Anthropic and one OpenAI model.
        assert!(seed.models.iter().any(|m| m.provider == "anthropic"));
        assert!(seed.models.iter().any(|m| m.provider == "openai"));
        // All seed entries must be on the two filtered providers
        // (§3.4.7).
        for m in &seed.models {
            assert!(
                m.provider == "anthropic" || m.provider == "openai",
                "unexpected provider {} in seed",
                m.provider
            );
        }
        // Every override target must reference a real seed entry
        // before we try to apply it. This catches typos at test time.
        for entry in &overrides.overrides {
            assert!(
                seed.models
                    .iter()
                    .any(|m| m.provider == entry.target.provider && m.id == entry.target.id),
                "override target {}/{} does not match any seed entry",
                entry.target.provider,
                entry.target.id
            );
        }
    }

    /// Loading the registry from the bundled seed must succeed and the
    /// overrides must take effect.
    #[test]
    fn load_real_registry_applies_overrides() {
        let reg = ModelRegistry::load();
        assert!(!reg.providers().is_empty());
        // Pick a model the overrides file flips: opus-4-7 should have
        // both flags set after override application.
        if let Some(m) = reg.get("anthropic", "claude-opus-4-7") {
            assert!(supports_xhigh(m), "opus-4-7 should support xhigh");
            assert!(
                supports_adaptive_thinking(m),
                "opus-4-7 should use adaptive thinking"
            );
        }
        // gpt-5.2 is OpenAI xhigh-capable per overrides.
        if let Some(m) = reg.get("openai", "gpt-5.2") {
            assert!(supports_xhigh(m), "gpt-5.2 should support xhigh");
        }
    }
