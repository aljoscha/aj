    /// The bundled seed and overrides are part of the binary; if either
    /// fails to parse the registry won't load. Regression-test that the
    /// committed JSON stays well-formed.
    #[test]
    fn bundled_seed_and_overrides_parse() {
        let seed: Catalog = serde_json::from_str(SEED_MODELS_JSON).expect("seed parses");
        let overrides: OverridesFile =
            serde_json::from_str(OVERRIDES_JSON).expect("overrides parse");
        // Seed must contain at least one Anthropic and one OpenAI model.
        assert!(seed.models.iter().any(|m| m.provider == "anthropic"));
        assert!(seed.models.iter().any(|m| m.provider == "openai"));
        // All seed entries must be on the two filtered providers.
        for m in &seed.models {
            assert!(
                m.provider == "anthropic" || m.provider == "openai",
                "unexpected provider {} in seed",
                m.provider
            );
        }
        // Every override target must reference a real seed entry
        // before we try to apply it. This catches typos at test time.
        for entry in &overrides.overrides {
            assert!(
                seed.models
                    .iter()
                    .any(|m| m.provider == entry.target.provider && m.id == entry.target.id),
                "override target {}/{} does not match any seed entry",
                entry.target.provider,
                entry.target.id
            );
        }
    }

