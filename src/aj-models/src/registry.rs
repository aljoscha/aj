/// Structural equality of two models by `(provider, api, id)`. Two
/// `ModelInfo`s with identical identity but different metadata still
/// compare equal — identity, not state, is what cross-provider replay
/// cares about.
pub fn models_are_equal(a: &ModelInfo, b: &ModelInfo) -> bool {
    a.provider == b.provider && a.api == b.api && a.id == b.id
}

/// Whether the model uses Anthropic's adaptive thinking API. Thin
/// accessor over [`ModelInfo::supports_adaptive_thinking`].
pub fn supports_adaptive_thinking(model: &ModelInfo) -> bool {
    model.supports_adaptive_thinking
}

/// Whether the model honours OpenAI's `text.verbosity` parameter.
/// Thin accessor over [`ModelInfo::supports_verbosity`].
pub fn supports_verbosity(model: &ModelInfo) -> bool {
    model.supports_verbosity
}

/// Validate a requested [`ThinkingLevel`] against a model's wire
/// vocabulary, returning a human-readable message when the level can't
/// be honoured.
///
/// aj sends the chosen effort verbatim — there is no remapping or
/// silent downgrade — so a level the provider's effort vocabulary
/// lacks is rejected here before the request is built:
///
/// - Anthropic adaptive models take an `effort` enum with no `minimal`
///   rung, so `Minimal` is rejected. Legacy budget-based Anthropic
///   models translate every level to a token budget and accept all of
///   them.
/// - The OpenAI-family APIs (Responses, Chat Completions, Codex) cap
///   at `xhigh`; `Max` is rejected.
///
/// Per-model effort support *within* a provider's vocabulary (e.g.
/// which adaptive Anthropic model accepts `max` vs `xhigh`, or whether
/// a given reasoning model supports `xhigh` at all) is deliberately
/// not modelled here: those requests are sent through and the provider
/// API returns its own 400.
///
/// Non-reasoning models ignore the level entirely (the providers omit
/// the thinking parameter), so any level is accepted as a no-op.
pub fn validate_thinking_level(model: &ModelInfo, level: &ThinkingLevel) -> Result<(), String> {
    if !model.reasoning {
        return Ok(());
    }
    match model.api.as_str() {
        "anthropic-messages" => {
            if model.supports_adaptive_thinking && matches!(level, ThinkingLevel::Minimal) {
                return Err(format!(
                    "model '{}' does not support thinking level 'minimal'; \
                     supported: low, medium, high, xhigh, max",
                    model.id
                ));
            }
            Ok(())
        }
        _ => {
            if matches!(level, ThinkingLevel::Max) {
                return Err(format!(
                    "model '{}' does not support thinking level 'max'; \
                     supported: minimal, low, medium, high, xhigh",
                    model.id
                ));
            }
            Ok(())
        }
    }
}

